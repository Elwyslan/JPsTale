	/**
	 * 绘制图片
	 * @return
	 */
	private static BufferedImage getImage() {
		final int sider = 5;// 边距
		int northestY =  (int) (79460 * scale);// pilai北部边界
		int southestY = (int) (22374 * scale);// 理查登南部边界
		final int cY = northestY + sider;
		final int height = northestY + southestY + sider * 2 + (int)(30000 * scale);
		
		int westX = (int) (19846 * scale);// 森林3西部边界
		int eastX = (int) (51829 * scale);// iron1东部边界
		final int cX = westX + sider;
		final int width = westX + eastX + sider * 2;
		
		// 绘图
		BufferedImage image = new BufferedImage(width, height, BufferedImage.TYPE_4BYTE_ABGR);
		Graphics2D g2 = (Graphics2D) image.getGraphics();
		g2.setPaint(Color.WHITE);
		g2.fillRect(0, 0, width, height);

		// 绘制地图边框
		for (int i = 0; i < data.length; i++) {
			double n = data[i][0] * scale;
			double s = data[i][1] * scale;
			double w = data[i][2] * scale;
			double e = data[i][3] * scale;
			
			int x = cX-(int)e;
			int y = cY+(int)n;
			int wid = (int)(-w+e);
			int hi = (int)(s - n);
			g2.setPaint(Color.BLUE);
			g2.drawRect(x, y, wid, hi);
		}
		for (int k=0; k<28; k++) {
			// 绘制刷怪点
			int[][] res = respawn[k];
			for (int i = 0; i < res.length; i++) {
				double dx = -res[i][1] * scale;
				double dy = -res[i][2] * scale;
				
				int x = cX-(int)dx;
				int y = cY+(int)dy;
				g2.setPaint(Color.RED);
				g2.fillOval(x-1, y-1, 5, 5);
			}
			// 绘制NPC
			res = npc[k];
			for (int i = 0; i < res.length; i++) {
				double dx = -res[i][0] * scale;
				double dy = -res[i][2] * scale;
				
				int x = cX-(int)dx;
				int y = cY+(int)dy;
				g2.setPaint(Color.GREEN);
				g2.fillOval(x-1, y-1, 5, 5);
			}
		}
		// 绘制地图名称
		for (int i = 0; i < data.length; i++) {
			double n = data[i][0] * scale;
			double s = data[i][1] * scale;
			double e = data[i][3] * scale;
			
			int x = cX-(int)e;
			int y = cY+(int)n;
			int hi = (int)(s - n);
			g2.setPaint(Color.RED);
			String str = String.format("%s: %.1f %.1f", names[i], e, n);
			g2.drawString(str, x + 8, y+6+hi/2);
		}
		
		// 绘制地图原点
		g2.setPaint(Color.ORANGE);
		g2.fill(new Ellipse2D(){
			@Override
			public Rectangle2D getBounds2D() {
				return null;
			}
			@Override
			public double getHeight() {
				return 10;
			}
			@Override
			public double getWidth() {
				return 10;
			}

			@Override
			public double getX() {
				return cX;
			}

			@Override
			public double getY() {
				return cY;
			}

			@Override
			public boolean isEmpty() {
				return true;
			}

			@Override
			public void setFrame(double arg0, double arg1, double arg2,
					double arg3) {
				
			}});
		
		return image;
	}
	
